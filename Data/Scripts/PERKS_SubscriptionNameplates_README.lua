    	Description:    
	This is intended to provide a quick way to add subscription status to player nameplates for a creator's game.
	This is a modified version of the default nameplates template provided by Core.
	
    This component comes with:
        1) PERKS_SubscriptionNameplates
        2) NameplateControllerClient
        3) Helper_Nameplate_With_Subscription_Marker
        4) Helper_SegmentSeparator
        
    Setup   
    =====
    
    1.	Set up the PERKS_GlobalTable with _G.PERKS.SUBSCRIPTION global variable (Refer to CosmeticShopAndPerks_README).
        
    2.	Drop PERKS_SubscriptionNameplates into the hierarchy
    
    3.	Adjust the custom properties under the PERKS_SubscriptionNameplates root folder to desired settings.
    
    4.	Navigate to the subscription custom properties located at the botton of the list of custom properties in
    	the PERKS_SubscriptionNameplates folder. Enable subscription perk and adjust the 
   
